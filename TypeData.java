
//type data pada java
//type data primitif -> byte, short, int, long, char

public class TypeData {
	public static void main(String[] args) {
		//type data number
		byte anilaiByte = 100;
		short anilaiShort = 1000;
		int anilaiInt = 1_000_000_000;
		long anilaiLong = 100_000_000_000L;
		
		System.out.println(anilaiByte);
		System.out.println(anilaiShort);
		System.out.println(anilaiInt);
		System.out.println(anilaiLong);
		
		//type data floating point
		float anilaiFloat = 12.12F;
		double anilaiDouble = 12.121212;
		
		System.out.println(anilaiFloat);
		System.out.println(anilaiDouble);
		
		//type data character
		char anilaiChar = 'a';
		
		System.out.println(anilaiChar);
		
		//type data boolean
		boolean anilaiBoolean = true;
		boolean annilaiBoolean = false;
		
		System.out.println(anilaiBoolean);
		System.out.println(annilaiBoolean);
		
		//type data string
		String ig = "Patrik_williamm";
		String firstName = "patrik";
		String lastName = "william";
		
		System.out.println(ig);
		System.out.println(firstName +" "+ lastName);

	}
}

